a = int(input('Digite um numero inteiro')) 
b = int(input('Digite outro numero inteiro'))


soma = a + b
sub = a - b 
mult = a * b
div = a / b 

print(f"O resultado da soma entre {a} e {b} é igual a {soma}. ")
print(f"O resultado da subtração entre {a} e {b} é igual a {sub}. ")
print(f"O resultado da multiplicação entre {a} e {b} é igual a {mult}. ")
print(f"O resultado da divisão entre {a} e {b} é igual a {div}. ")


